<template>
  <span>Время нахождения внутри: <span v-bind:class="statusColor">{{ toString() }}</span></span>
</template>

<script>
  const UPDATE_INTERVAL = 10
  
  export default {
    name: 'time-counter-color',
    props: ['fromTime'],
    data() {
      return {
        now: Math.trunc(Date.now() / 1000)
      }
    },
    computed: {
      statusColor: function () {
        let _time = this.fromTime
        let timeDiff = (this.now - _time)/60
        switch (true) {
          case (timeDiff > 60): return 'gt60'
          case (timeDiff > 40): return 'gt40'
          default: return ''
        }
      }
    },
    methods: {
      toString: function () {
        let pad = (i) => { return ("0" + i).substr(-2) }
        let _time = this.fromTime
        let timeDiff = this.now - _time
        let days = Math.floor(timeDiff/(3600*24))
        let hours = Math.floor((timeDiff % (3600*24))/3600)
        let minutes = pad(Math.floor(((timeDiff % (3600*24))%3600)/60)) + 'мин.'
        let result = hours > 0 ? pad(hours) + 'ч:' + minutes : minutes
        return days > 0 ? pad(days) + 'д.:' + result : result
      }
    },
    mounted() {
      setInterval(() => { this.now = Math.trunc(Date.now() / 1000) }, UPDATE_INTERVAL * 1000)
    }
  }
</script>

<style lang="scss" scoped>
  span {
    color: white;
    
    &.gt40 {
      color: black;
      background-color: yellow;
    }
    &.gt60 {
      color: white;
      background-color: darkred;
    }
  }
</style>
